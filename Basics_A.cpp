#include <iostream>

using namespace std;

int main1()
{
    // Standard Hello World print
    std::cout << "Hello World!\n";

    // Declaring a variable
    int varInt;

    // Printing a message to the console screen/window
    cout << "Enter an integer" << endl;

    // Gathering input with a variable
    cin >> varInt;

    // Outputting input from stored variable
    cout << varInt << " is your number" << endl;

    return 0;

}

